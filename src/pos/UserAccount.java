    private void btnPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPasswordActionPerformed
        // TODO add your handling code here:
        String password = fn.getPassword(jpfPassword);
        String confirmPassword = fn.getPassword(jpfConfirmPassword);
        
        if(password.length() > 0 && confirmPassword.length() > 0){
            
            if(password.equals(confirmPassword)){
                if(user.getRoleName() != null){
                    int passwordChanged = UserBLL.ChangePassword(user.getUserName(), password);
                    if(passwordChanged > 0){
                        fn.successMessage("Password changed!");
                    }else{
                        fn.errorMessage("Password not changed!");
                    }
                }else{
                    fn.errorMessage("Selected user details could not be determined.\n"
                            + "Save account before changing password!");
                }
            }else{
                fn.warningMessage("Password must match!");
            }
        }else{
            fn.warningMessage("Password fields must not be empty!");
        } 
    }//GEN-LAST:event_btnPasswordActionPerformed

