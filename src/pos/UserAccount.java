    private void btnSaveAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveAccountActionPerformed
        // TODO add your handling code here:
        
        String email = txtEmail.getText();
        boolean isLocked = jcbIsLocked.isSelected();
        int roleID = userRoleSelected.getRoleID();
        
        int locked = (isLocked)? 1:0;
        
        if(user.getRoleName() == null){
            int accountAdded = UserBLL.AddUserAccount(user.getUserName(), email, locked, loggedInUser.getUserName(), roleID);
            if(accountAdded > 0){
                fn.successMessage("Account added!");
                UserBLL.refreshUsers();
                user = UserBLL.getUser(user.getUserName());
            }else{
                fn.errorMessage("Account not added!");
            }
        }else{
            int accountEdited = UserBLL.EditUserAccount(user.getUserName(), email, locked, roleID);
            if(accountEdited > 0){
                fn.successMessage("Account edited!");
            }else{
                fn.errorMessage("Account not edited!");
            }
        }     
    }//GEN-LAST:event_btnSaveAccountActionPerformed

