    public final void showUserData(){
        lblUsername.setText(user.getUserName());
        lblFirstname.setText(user.getFirstName());
        lblLastname.setText(user.getLastName());
        lblTelephone.setText(user.getTelephone());
        txtEmail.setText(user.getEmail());
        if(user.getIsLocked() == 1){
            jcbIsLocked.setSelected(true);
            System.out.println("Locked");
        }else{
            jcbIsLocked.setSelected(false);
        }
        
        RoleBLL.fillComboBoxUserRole(jcbRoles);
        RoleBLL.setSelectedUserRole(jcbRoles, RoleBLL.getUserRole(user.getRoleID()));
        
        
        if(createdBy != null){
            lblCreateBy.setText(user.getCreateBy()+" - "+createdBy.toString());
        }else{
            userRoleSelected = RoleBLL.getFirstUserRole();
            RoleBLL.setSelectedUserRole(jcbRoles, userRoleSelected);
        }
    }

